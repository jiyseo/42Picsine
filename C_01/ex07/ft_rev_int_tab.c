void	ft_rev_int_tab(int *tab, int size)
{
	int	a;
	int	i;
	int	j;

	i = 0;
	j = size - 1;
	size /= 2;
	while (size > 0)
	{
		a = tab[i];
		tab[i] = tab[j];
		tab[j] = a;
		i++;
		j--;
		size --;
	}
}
