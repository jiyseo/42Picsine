char	*ft_strcat(char	*dest, char	*src)
{
	char	*p;

	p = dest;
	while (*p)
	{
		p++;
	}
	while (*src != '\0')
	{	
		*p++ = *src++;
	}
	*p = '\0';
	return (dest);
}
