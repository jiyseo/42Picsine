char	*ft_strncat(char	*dest, char	*src, unsigned int	nb)
{
	char	*p;

	p = dest;
	while (*p)
	{
		p++;
	}
	while (nb > 0 && *src != '\0')
	{	
		*p++ = *src++;
		nb--;
	}
	*p = '\0';
	return (dest);
}
